      let app = new Vue({
        el: '#vue-app',
        data: {
          guest_limits: {
            pithi_guest_limit: 0,
            garba_guest_limit: 0,
            wedding_guest_limit: 0,
            reception_guest_limit: 0,
            ganesh_sthapana_guest_limit: 0,
          },
          form: {
            phone_number: '',
            name: '',
            email: '',
            message: '',
            pithi_guests: 0,
            garba_guests: 0,
            ganesh_sthapana_guests: 0,
            wedding_guests: 0,
            reception_guests: 0,
            covid_checked: false,
          },
          eventsForm: {
            phone_number: '',
            name: ''
          },
          showActionButtons: true,
          showRsvpPart1: false,
          showRsvpPart2: false,
          showRsvpThankYou: false,
          showEvents: false,
          showGuestEventsForm: false,
          showEventsThankYou: false,
          rsvpLoading: false,
        },
        computed: {
          showAllEvents() {
            if (!this.showEvents)
              return false;

            return this.showGarbaEvent || this.showWeddingEvent || this.showReceptionEvent || this.showPithiEvent || this.showGaneshEvent; 
          },
          showGarbaEvent() {
            return this.showRsvpThankYou ? this.form.garba_guests > 0
            : this.guest_limits.garba_guest_limit > 0;
          },
          showGaneshEvent() {
            return this.showRsvpThankYou ? this.form.ganesh_sthapana_guests > 0
            : this.guest_limits.ganesh_sthapana_guest_limit > 0;
          },
          showPithiEvent() {
            return this.showRsvpThankYou ? this.form.pithi_guests > 0
            : this.guest_limits.pithi_guest_limit > 0;
          },
          showWeddingEvent() {
            return this.showRsvpThankYou ? this.form.wedding_guests > 0
            : this.guest_limits.wedding_guest_limit > 0;
          },
          showReceptionEvent() {
            return this.showRsvpThankYou ? this.form.reception_guests > 0
            : this.guest_limits.reception_guest_limit > 0;
          },
          daysToGo() {
          let today = dateFns.startOfToday();
          let weddingDate = Date.parse("19 June 2022 14:00:00 GMT");
          return dateFns.differenceInDays(weddingDate, today); 
          },
        },
        methods: {
          formattedPhoneNumber() {
            return this.form.phone_number.replaceAll('-', '');
          },
          formattedEventsPhoneNumber() {
            return this.eventsForm.phone_number.replaceAll('-', '');
          },
          formattedName() {
            let name = $('#name').val();
            if (!name)
              return '';

            return name.replaceAll(' and Family', '');
          },
          formattedEventsName() {
            return $('#events_name').val().replaceAll(' and Family', '');
          },
          showRsvpForm: function(fromEvents=false) {
            this.showRsvpPart1 = true;
            this.showActionButtons = false;
            if (fromEvents)
              this.showEventsThankYou = false;

            setTimeout(() => {
              $('html, body').animate({
                scrollTop: $('.ww-rsvp-form').offset().top
              }, 2000);  
            }, 200);
          },
          showEventsForm: function() {
            this.showGuestEventsForm = true;
            this.showActionButtons = false;
            setTimeout(() => {
              $('html, body').animate({
                scrollTop: $('#events-form-title').offset().top
              }, 2000);  
            }, 200);
          },
          rsvpPart1Submit: function() {
            $.getJSON("LookupGuestLimit.php", { 
              phone_number: this.formattedPhoneNumber(), 
              name: this.formattedName() 
            }, (response) => {
              this.guest_limits.pithi_guest_limit = parseInt(response.pithi_guest_limit);
              this.guest_limits.ganesh_sthapana_guest_limit = parseInt(response.ganesh_sthapana_guest_limit);
              this.guest_limits.garba_guest_limit = parseInt(response.garba_guest_limit);
              this.guest_limits.wedding_guest_limit = parseInt(response.wedding_guest_limit);
              this.guest_limits.reception_guest_limit = parseInt(response.reception_guest_limit);
              this.showRsvpPart1 = false;
              this.showRsvpPart2 = true;
              setTimeout(() => {
              $('html, body').animate({
                scrollTop: $('.form-part-2').offset().top
              }, 2000);  
            }, 200);
            })
            .fail(function(error) {
              alert(error.responseText);
            });
          },
          rsvpPart2Submit: async function() {
            // if (!this.form.covid_checked) {
            //   alert('Please check the COVID-19 acknowledgement checkbox.')
            //   return;
            // }
            let form = {
              ...this.form,
              name: this.formattedName(),
              phone_number: this.formattedPhoneNumber()
            };
            this.rsvpLoading = true;
            $.post("CreateRSVP.php", form, async (response) => {
              this.rsvpLoading = false;
              if (response === "OK") {
                this.showRsvpPart2 = false;
                this.showRsvpThankYou = true;
                this.showEvents = true;
                await this.navigateToEvents(true);
                myalert();
              }
            });
          },
          eventsFormSubmit: function() {
            $.getJSON("LookupGuests.php", { 
              phone_number: this.formattedEventsPhoneNumber(), 
              name: this.formattedEventsName() 
            }, (response) => {
              this.guest_limits.pithi_guest_limit = parseInt(response.pithi_guests);
              this.guest_limits.ganesh_sthapana_guest_limit = parseInt(response.ganesh_sthapana_guests);
              this.guest_limits.garba_guest_limit = parseInt(response.garba_guests);
              this.guest_limits.wedding_guest_limit = parseInt(response.wedding_guests);
              this.guest_limits.reception_guest_limit = parseInt(response.reception_guests);
              this.navigateToEvents(false);
              this.showEvents = true;
              this.showEventsThankYou = true;
              this.showGuestEventsForm = false;
              $('.autocomplete-select').blur();
            })
            .fail(function(error) {
              alert(error.responseText);
            });
          },
          navigateToEvents: async function(fromRSVP=false) {
            let element = '#rsvp';
            if (this.showAllEvents)
              element = '#events-section';

            await new Promise(resolve => setTimeout(resolve, 200));

            await $('html, body').animate({
              scrollTop: $(element).offset().top + (fromRSVP ? 0 : 425)
            }, 2000).promise();
          }
        }
      });
